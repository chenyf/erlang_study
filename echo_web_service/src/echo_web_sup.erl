-module(echo_web_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([
		 start_link/1,
		 start_child/0
		]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================

start_link(Port) ->
	supervisor:start_link({local, ?SERVER}, ?MODULE, [Port]).

start_child() ->
	supervisor:start_child(?SERVER, []).



%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([Port]) ->
	Server = {
			  echo_web_server,
			  {echo_web_server, start_link, [Port]},
			  permanent, 2000, worker, [echo_web_server]
			 },
	RS = {one_for_one, 0, 1},
    {ok,{RS, [Server]}}.

